class TestSlugifyDeye:
    """Test 5: slugify() matches reference for every item name in deye_micro.yaml."""

    def test_all_names_match_reference(self):
        # APPROACH: pre-computed reference dict (HA not available in this env)
        # Reference generated using solarman_logger.common.slugify(name, "sensor")
        names = _load_profile_names("deye_micro.yaml")
        # Deduplicate while preserving order (some names appear twice in different groups)
        seen = set()
        for name in names:
            if name in seen:
                continue
            seen.add(name)
            result = slugify(name, "sensor")
            if name in DEYE_MICRO_REFERENCE:
                assert result == DEYE_MICRO_REFERENCE[name], (
                    f"slugify({name!r}, 'sensor') = {result!r}, "
                    f"expected {DEYE_MICRO_REFERENCE[name]!r}"
                )
            # Also assert structural validity
            assert _is_valid_slug(result), (
                f"slugify({name!r}, 'sensor') = {result!r} contains invalid characters"
            )

    def test_all_names_produce_valid_slugs(self):
        names = _load_profile_names("deye_micro.yaml")
        for name in names:
            result = slugify(name, "sensor")
            assert isinstance(result, str), f"slugify({name!r}) should return str"
            assert len(result) > 0, f"slugify({name!r}) should not return empty string"
            assert _is_valid_slug(result), (
                f"slugify({name!r}) = {result!r} is not a valid slug "
                f"(expected ASCII lowercase + underscores/digits only)"
            )


