# Absolute path to inverter definitions directory
# Path: solarman_logger/tests/ -> ../../ -> repo root -> config/inverter_definitions/
INVERTER_DEFS_DIR = os.path.abspath(
    os.path.join(os.path.dirname(__file__), "../../config/inverter_definitions/")
)


def _load_profile_names(filename: str) -> list[str]:
    """Load all item names from a YAML profile file."""
    with open(os.path.join(INVERTER_DEFS_DIR, filename)) as f:
        profile = yaml.safe_load(f)
    return [
        item["name"]
        for group in profile["parameters"]
        for item in group["items"]
        if "name" in item
    ]


def _is_valid_slug(slug: str) -> bool:
    """Check that a slug contains only ASCII lowercase, digits, and underscores."""
    return bool(re.match(r"^[a-z0-9_]+$", slug)) if slug else False


